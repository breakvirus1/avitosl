#!/bin/bash

# Скрипт для убийства процессов микросервисов и фронта по портам

# Порты микросервисов
PORTS=(8081 8082 8083 8084 8085 8086 5173 8761)

echo "Убиваем процессы на портах: ${PORTS[*]}"

for PORT in "${PORTS[@]}"; do
    # Находим PID процесса, слушающего порт
    PID=$(lsof -ti :$PORT)
    if [ -n "$PID" ]; then
        echo "Убиваем процесс на порту $PORT (PID: $PID)"
        kill -9 $PID
    else
        echo "Нет процесса на порту $PORT"
    fi
done

echo "Готово."